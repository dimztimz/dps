

exponeneti = [-9:3];
koeficientiVoOdnosA4 = 2.^(exponeneti/12);
A4 = 440;
tonovi = A4*koeficientiVoOdnosA4;
skalaCDur = [tonovi(1:2:5) tonovi(6:2:12) tonovi(13)];
freqSempliranje = 8192;
vreme = 1;
matAgli=skalaCDur' * ([1:freqSempliranje*vreme]/freqSempliranje);
zvuci = sin(2*pi*matAgli);
%zvuk = zvuci'(:);
pesna = [zvuci(1,:) zeros(1,10) zvuci(1,:) zeros(1,10)...
 zvuci(5,:) zeros(1,10) zvuci(5,:) zeros(1,10)...
 zvuci(6,:) zeros(1,10) zvuci(6,:) zeros(1,10)...
 zvuci(5,:) zvuci(5,:) zeros(1,10)...
 zvuci(4,:) zeros(1,10) zvuci(4,:) zeros(1,10)...
 zvuci(3,:) zeros(1,10) zvuci(3,:) zeros(1,10)...
 zvuci(2,:) zeros(1,10) zvuci(2,:) zeros(1,10)...
 zvuci(1,:) zvuci(1,:) zeros(1,10)];
wavwrite(pesna, freqSempliranje, 'pesna.wav');
system pesna.wav;